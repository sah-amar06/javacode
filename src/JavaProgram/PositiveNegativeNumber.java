package JavaProgram;

public class PositiveNegativeNumber {

	public static void main(String[] args) {
		
		double num = 0;		//Number could be anything
		
		if(num>0.0) {
			System.out.println("Given number is positive number: " +num);
		}else if(num<0.0){
			System.out.println("Given number is negative number: " +num);
		}else {
			System.out.println("Given number is zero: "+num);
		}
		

	}

}
